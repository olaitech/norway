import fs from "node:fs";
import path from "node:path";

const appDir = path.join(process.cwd(), "app");
const layoutPath = path.join(appDir, "layout.tsx");
const robotsPath = path.join(appDir, "robots.ts");
const sitemapPath = path.join(appDir, "sitemap.ts");

const expectedRoutes = [
  "/",
  "/destinations",
  "/destinations/lofoten-islands",
  "/destinations/senja",
  "/destinations/helgeland-coast",
  "/destinations/tromso",
  "/routes",
  "/journal",
  "/map",
  "/best-time-to-visit-norway",
  "/norway-road-trip-routes",
  "/lofoten-travel-guide",
  "/northern-lights-norway",
  "/fjords-of-norway",
  "/norway-itinerary-7-days",
  "/norway-itinerary-10-days",
  "/responsible-travel",
  "/about",
];

const defaultMetadataPatterns = [
  "Create Next App",
  "Generated by create next app",
];

let errors = 0;
let warnings = 0;
let missingRoutes = 0;

function readText(filePath) {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, "utf8") : "";
}

function hasPageFile(dirPath) {
  return (
    fs.existsSync(path.join(dirPath, "page.tsx")) ||
    fs.existsSync(path.join(dirPath, "page.ts")) ||
    fs.existsSync(path.join(dirPath, "page.jsx")) ||
    fs.existsSync(path.join(dirPath, "page.js"))
  );
}

function dynamicSegmentDirs(dirPath) {
  if (!fs.existsSync(dirPath)) {
    return [];
  }

  return fs
    .readdirSync(dirPath, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && entry.name.startsWith("[") && entry.name.endsWith("]"))
    .map((entry) => path.join(dirPath, entry.name));
}

function resolveRoute(route) {
  if (route === "/") {
    const exists = hasPageFile(appDir);
    return {
      exists,
      source: exists ? "app/page.tsx" : "app/page.tsx",
    };
  }

  let candidates = [{ dir: appDir, sourceParts: ["app"] }];

  for (const segment of route.split("/").filter(Boolean)) {
    const nextCandidates = [];

    for (const candidate of candidates) {
      const directDir = path.join(candidate.dir, segment);
      if (fs.existsSync(directDir) && fs.statSync(directDir).isDirectory()) {
        nextCandidates.push({
          dir: directDir,
          sourceParts: [...candidate.sourceParts, segment],
        });
      }

      for (const dynamicDir of dynamicSegmentDirs(candidate.dir)) {
        nextCandidates.push({
          dir: dynamicDir,
          sourceParts: [...candidate.sourceParts, path.basename(dynamicDir)],
        });
      }
    }

    candidates = nextCandidates;
  }

  const match = candidates.find((candidate) => hasPageFile(candidate.dir));

  return {
    exists: Boolean(match),
    source: match ? `${match.sourceParts.join("/")}/page.tsx` : `app${route}/page.tsx`,
  };
}

function parseSitemapRoutes(source) {
  const routes = new Set();
  const quotedStringRegex = /["'`]([^"'`]+)["'`]/g;
  let match;

  while ((match = quotedStringRegex.exec(source)) !== null) {
    const value = match[1];

    if (value === "/" || value.startsWith("/")) {
      routes.add(value);
      continue;
    }

    try {
      const url = new URL(value);
      routes.add(url.pathname);
    } catch {
      // Ignore non-URL strings.
    }
  }

  return routes;
}

function ok(message) {
  console.log(`[ok] ${message}`);
}

function warn(message) {
  warnings += 1;
  console.warn(`[warn] ${message}`);
}

function error(message) {
  errors += 1;
  console.error(`[error] ${message}`);
}

console.log("SEO foundation check\n");

console.log("Core files");
if (fs.existsSync(layoutPath)) {
  ok("app/layout.tsx exists");
} else {
  error("app/layout.tsx is missing");
}

if (fs.existsSync(robotsPath)) {
  ok("app/robots.ts exists");
} else {
  error("app/robots.ts is missing");
}

if (fs.existsSync(sitemapPath)) {
  ok("app/sitemap.ts exists");
} else {
  error("app/sitemap.ts is missing");
}

const layoutSource = readText(layoutPath);
const robotsSource = readText(robotsPath);
const sitemapSource = readText(sitemapPath);
const sitemapRoutes = parseSitemapRoutes(sitemapSource);

for (const pattern of defaultMetadataPatterns) {
  if (layoutSource.includes(pattern)) {
    error(`app/layout.tsx still contains default metadata: "${pattern}"`);
  }
}

if (layoutSource && defaultMetadataPatterns.every((pattern) => !layoutSource.includes(pattern))) {
  ok("app/layout.tsx does not contain default Next.js metadata strings");
}

if (robotsSource) {
  if (/sitemap/i.test(robotsSource)) {
    ok("app/robots.ts references the sitemap");
  } else {
    error("app/robots.ts does not reference the sitemap");
  }
}

console.log("\nExpected routes");
const routeResults = expectedRoutes.map((route) => {
  const result = resolveRoute(route);

  if (result.exists) {
    ok(`${route} exists via ${result.source}`);
  } else {
    missingRoutes += 1;
    console.log(`[missing] ${route} is not implemented in app/`);
  }

  return { route, ...result };
});

console.log("\nSitemap coverage for existing expected routes");
for (const result of routeResults) {
  if (!result.exists) {
    continue;
  }

  if (sitemapRoutes.has(result.route)) {
    ok(`${result.route} is listed in app/sitemap.ts`);
  } else {
    warn(`${result.route} exists but is not listed in app/sitemap.ts`);
  }
}

console.log("\nSummary");
console.log(`Errors: ${errors}`);
console.log(`Warnings: ${warnings}`);
console.log(`Missing expected routes: ${missingRoutes}`);

if (errors > 0) {
  process.exitCode = 1;
}
